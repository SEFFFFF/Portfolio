            <h3>Library research</h3>
            <p>Als eerste begon ik met het onderzoeken wat het inhoud om een ondernemer te zijn. Ik las het artikel wat op canvas stond maar hier kon ik niet veel nuttige informatie uithalen. Gelukkig kwam ik al snel bij een artikel van KVK wat per stap beschrijft wat je nodig heb om een bedrijf te beginnen. Hier heb ik me de tijd voor genomen om dit uit te werken.</p>
