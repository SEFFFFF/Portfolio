            <h3>Field research</h3>
            <p>Toevallig zit er in onze klas een jongen, Soulaiman Meskini, die al een eigen merk heeft dus het leek me perfect om hem te benaderen en wat vragen te stellen. Ik heb met hem een dik uur gesproken over hoe je het beste je product kan marketen (instagram, influencers benaderen en weten wie je doelgroep is), wat goede manieren van productie zijn (hij gebruikt een print on demand service) en hoe je klanten behoud (marketing na aankoop van een product en interactie met je klanten). </p>
